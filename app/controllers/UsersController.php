	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{

        $member = Member::where('member_no','=',Input::get('memberNo'))->firstOrFail();
        $user = new User;
        $user->first_name = Input::get('memberFName');
        $user->last_name = Input::get('memberLName');
        $user->memberId = $member->id;
        $user->save();
        return Redirect::to('users/create')->with('flash_message', 'User Record Created Successfully.');
	}

